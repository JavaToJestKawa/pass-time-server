package zad1;

public class PassedTimeInCalendarNotation {
    public final int yearsPeriod;
    public final int monthsPeriod;
    public final int daysPeriod;

    public PassedTimeInCalendarNotation(int yearsPeriod, int monthsPeriod, int daysPeriod) {
        this.yearsPeriod = yearsPeriod;
        this.monthsPeriod = monthsPeriod;
        this.daysPeriod = daysPeriod;
    }
}
